#예제 : Pithon 문자열을 Python으로 바꾸러면?
a = "pithon"
print(a[1])
#이렇개 하면 에러가 난다 
# a[1] = 'y'
# why? : 문자열의 요솟값은 바꿀 수 있는 값이 아니다. 문자열 자료형은 변경 불가하기 때문데 immutable한 자료형이다.,

#따라서 이렇게 한다.
a = "pithon"
print(a[:1])
print(a[2:])

print(a[:1] + 'y' + a[2:])

    