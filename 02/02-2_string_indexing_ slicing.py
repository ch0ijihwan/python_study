#문자열 인덱싱
a = "Life is too short, You need Python"
print(a[3])

#문자열 인덱싱 활용하기
print(a[0])
print(a[12])


# 파이썬에서 문자열의 뒤에서 첫번째는 -1이다.
print(a[-1])

#뒤에서 두번째 숫자는 -2 이다.
print(a[-2])
#뒤에서 다섯번째 숫자는 -5 이다.
print(a[-5])

