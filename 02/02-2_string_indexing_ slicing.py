#문자열 슬라이싱 
a = "Life is too short, You need Python"
b = a[0]+a[1]+a[2]+a[3]
print(b)

# 슬라이싱 예제
# 문자열 a에서 0번부터 4번 까지의 문자를 뽑아낸 것
print(a[0:4])
# 문자열 a에서 0번부터 3번 까지의 문자를 뽑아낸 것
print(a[0:3])
# 문자열 a에서 0번부터 5번 까지의 문자를 뽑아낸 것
print(a[0:5])

#슬라이싱 할 때 항상 시작 번호가 0일 필요는 없다.
print(a[0:2])

print(a[5:7])

print(a[12:17])

#a[시작 번호 : 끝 번호]에서 끝 번호 부분을 생략하면 시작 번호부터 그 문자열의 끝까지 뽑아낸다.
print(a[19:])

#a[시작 번호 : 끝 번호]에서 시작 변호를 생략하면 문자열의 처음부터 끝 번호까지 뽑아낸다.
print(a[:17])

# a[시작 번호 : 끝 번호]에서 시작 번호와 끝 번호를 생략하면 문자열 전체를 출력한다.
print(a[:])

# 슬라이싱에서도 인덱싱과 마찬가지로 - 기호 사용가능
print(a[19:-7])


